    #[test]
    fn thousand_symbols_snapshot_plus_delta_all_non_empty() {
        let mut h = SyntheticHarness::with_symbol_count(1_000, 5678);
        h.broadcast_snapshot(3, 3);
        h.broadcast_delta(2, 2);

        for i in 1..=1_000usize {
            let book = h.book(&format!("SYM{i:06}")).unwrap();
            assert!(!book.is_empty(), "SYM{i:06} must not be empty");
        }
        assert!(h.check_invariants().is_clean());
    }

    // -----------------------------------------------------------------------
    // Edge case: zero updates
    // -----------------------------------------------------------------------

    #[test]
    fn zero_update_advances_sequence_but_leaves_book_unchanged() {
        let mut h = SyntheticHarness::with_symbols(["BTCUSDT"], 1);

        h.apply_snapshot_for("BTCUSDT", 3, 3);
        let bid_depth_before = h.book("BTCUSDT").unwrap().bid_depth();
        let ask_depth_before = h.book("BTCUSDT").unwrap().ask_depth();
        let uid_before = h.book("BTCUSDT").unwrap().last_update_id();

        let new_uid = h.apply_zero_update("BTCUSDT");

        let book = h.book("BTCUSDT").unwrap();
        assert_eq!(
            book.bid_depth(),
            bid_depth_before,
            "zero update must not change bid depth"
        );
        assert_eq!(
            book.ask_depth(),
            ask_depth_before,
            "zero update must not change ask depth"
        );
        assert!(book.last_update_id() > uid_before, "update_id must advance");
        assert_eq!(
            book.last_update_id(),
            new_uid,
            "returned uid must match book"
        );
    }

    #[test]
    fn repeated_zero_updates_do_not_corrupt_book() {
        let mut h = SyntheticHarness::with_symbols(["ETHUSDT"], 2);

        h.apply_snapshot_for("ETHUSDT", 5, 5);
        for _ in 0..100 {
            h.apply_zero_update("ETHUSDT");
        }

        let book = h.book("ETHUSDT").unwrap();
        assert_eq!(book.bid_depth(), 5);
        assert_eq!(book.ask_depth(), 5);
        assert!(h.check_invariants().is_clean());
    }

    // -----------------------------------------------------------------------
    // Edge case: wide spreads
    // -----------------------------------------------------------------------

    #[test]
    fn wide_spread_both_sides_non_empty_bid_strictly_below_ask() {
        let mut h = SyntheticHarness::with_symbols(["BTCUSDT"], 3);
        h.apply_wide_spread("BTCUSDT");

        let book = h.book("BTCUSDT").unwrap();
        let bb = book.best_bid().expect("bid must be present");
        let ba = book.best_ask().expect("ask must be present");

        assert_eq!(bb.price, WIDE_BID_PRICE, "bid must be at WIDE_BID_PRICE");
        assert_eq!(ba.price, WIDE_ASK_PRICE, "ask must be at WIDE_ASK_PRICE");
        assert!(
            bb.price < ba.price,
            "bid must be strictly below ask even for wide spreads"
        );
        assert!(h.check_invariants().is_clean());
    }

    #[test]
    fn wide_spread_quantifies_the_gap() {
        let mut h = SyntheticHarness::with_symbols(["SOLUSDT"], 4);
        h.apply_wide_spread("SOLUSDT");

        let book = h.book("SOLUSDT").unwrap();
        let spread = book.best_ask().unwrap().price - book.best_bid().unwrap().price;
        assert!(spread > 1_000, "spread should be very wide, got {spread}");
    }

    // -----------------------------------------------------------------------
    // Edge case: empty bid side
