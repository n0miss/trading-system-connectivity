    #[test]
    fn hundred_symbols_broadcast_delta_storm() {
        let mut h = SyntheticHarness::with_symbol_count(100, 99);

        h.broadcast_snapshot(10, 10);
        for _ in 0..20 {
            h.broadcast_delta(3, 3);
        }

        assert_eq!(h.stats.snapshots_applied, 100);
        assert_eq!(h.stats.deltas_applied, 2_000);
        assert!(
            h.check_invariants().is_clean(),
            "no invariant violations expected"
        );
    }

    #[test]
    fn hundred_symbols_zero_updates_leave_books_unchanged() {
        let mut h = SyntheticHarness::with_symbol_count(100, 100);

        h.broadcast_snapshot(5, 5);

        let depths_before: Vec<(usize, usize)> = (1..=100usize)
            .map(|i| {
                let b = h.book(&format!("SYM{i:06}")).unwrap();
                (b.bid_depth(), b.ask_depth())
            })
            .collect();

        // Zero updates don't change depths.
        h.broadcast_zero_update();

        let depths_after: Vec<(usize, usize)> = (1..=100usize)
            .map(|i| {
                let b = h.book(&format!("SYM{i:06}")).unwrap();
                (b.bid_depth(), b.ask_depth())
            })
            .collect();

        assert_eq!(depths_before, depths_after);
    }

