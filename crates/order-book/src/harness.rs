/// Synthetic test harness for order-book scenarios (§10.37).
///
/// Generates deterministic [`BookSnapshot`] and [`BookDelta`] messages and
/// drives them through one or more [`OrderBook`] instances.  Used in:
///
/// * **Stage 10.37** — scale (1 / 10 / 100 / 1000 symbols) and edge-case tests.
/// * **Stage 10.38** — property tests (bid < ask, no negative qty, monotonic seq).
/// * **Stage 10.39** — chaos tests (fault injection via the replay crate).
///
/// # Price convention
///
/// All synthetic prices are raw `i64` scaled integers representing
/// **units × 1000** (three implied decimal places).  Bids descend from
/// `MID_PRICE - TICK` downward; asks ascend from `MID_PRICE + TICK` upward.
/// This guarantees the bid side never crosses the ask side in normal scenarios.
use std::collections::HashMap;

use connector_core::{
    BookDelta, BookSnapshot, MarketType, MessageHeader, MessageType, PriceLevel, VenueId,
    SCHEMA_VERSION, TS_NONE, UPDATE_ID_NONE,
};

use crate::OrderBook;

// ---------------------------------------------------------------------------
// Price constants
// ---------------------------------------------------------------------------

/// Midpoint around which synthetic bid/ask levels are generated.
pub const MID_PRICE: i64 = 100_000;
/// Minimum price separation between adjacent synthetic levels.
pub const TICK_SIZE: i64 = 100;
/// Bid price used by `apply_wide_spread` — one tick above zero.
pub const WIDE_BID_PRICE: i64 = TICK_SIZE;
/// Ask price used by `apply_wide_spread` — far above the mid.
pub const WIDE_ASK_PRICE: i64 = 1_000_000_000;

// ---------------------------------------------------------------------------
// Xorshift64 PRNG (local copy — avoids a crate dependency for a tiny struct)
// ---------------------------------------------------------------------------

struct Prng {
    state: u64,
}

impl Prng {
    fn new(seed: u64) -> Self {
        Self {
            state: if seed == 0 {
                0xDEAD_BEEF_CAFE_F00D
            } else {
                seed
            },
        }
    }

    fn next(&mut self) -> u64 {
        let mut x = self.state;
        x ^= x << 13;
        x ^= x >> 7;
        x ^= x << 17;
        self.state = x;
        x
    }

    /// Positive quantity in [1, 1000].
    fn qty(&mut self) -> i64 {
        1 + (self.next() % 1_000) as i64
    }
}

// ---------------------------------------------------------------------------
// HarnessStats
// ---------------------------------------------------------------------------

/// Aggregate event counts for a [`SyntheticHarness`] session.
#[derive(Debug, Clone, Default, PartialEq, Eq)]
pub struct HarnessStats {
    pub snapshots_applied: u64,
    pub deltas_applied: u64,
}

// ---------------------------------------------------------------------------
// InvariantViolation
// ---------------------------------------------------------------------------

/// A detected invariant violation in one of the harness's books.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum InvariantViolation {
    /// `best_bid.price >= best_ask.price` — the book is crossed.
    BidAskCrossed {
        symbol: String,
        bid_price: i64,
        ask_price: i64,
    },
    /// A retained level has `qty <= 0` — the book stored an invalid entry.
    NonPositiveQty {
        symbol: String,
        side: &'static str,
        price: i64,
        qty: i64,
    },
}

/// Collection of invariant violations found by [`SyntheticHarness::check_invariants`].
#[derive(Debug, Default)]
pub struct InvariantViolations {
    pub violations: Vec<InvariantViolation>,
}

impl InvariantViolations {
    /// `true` when no violations were found.
    pub fn is_clean(&self) -> bool {
        self.violations.is_empty()
    }
    pub fn count(&self) -> usize {
        self.violations.len()
    }
}

// ---------------------------------------------------------------------------
// SyntheticHarness
// ---------------------------------------------------------------------------

/// Maintains a set of [`OrderBook`]s and drives them with deterministic
/// synthetic events.
///
/// All price and quantity values are generated by a seeded PRNG so replays
/// are fully reproducible.
pub struct SyntheticHarness {
    books: HashMap<String, OrderBook>,
    /// Monotonically increasing sequence number, used as `last_update_id`.
    seq: u64,
    prng: Prng,
    pub stats: HarnessStats,
}

impl SyntheticHarness {
    // ------------------------------------------------------------------
    // Constructors
    // ------------------------------------------------------------------

    /// Create an empty harness with no registered symbols.
    pub fn new(seed: u64) -> Self {
        Self {
            books: HashMap::new(),
            seq: 0,
            prng: Prng::new(seed),
            stats: HarnessStats::default(),
        }
    }

    /// Create a harness pre-populated with `n` synthetic symbols named
    /// `"SYM000001"`, `"SYM000002"`, … `"SYMnnnnnn"`.
    pub fn with_symbol_count(n: usize, seed: u64) -> Self {
        let mut h = Self::new(seed);
        h.add_symbol_count(n);
        h
    }

    /// Create a harness pre-populated with the given symbol names.
    pub fn with_symbols<S: Into<String>>(symbols: impl IntoIterator<Item = S>, seed: u64) -> Self {
        let mut h = Self::new(seed);
        for sym in symbols {
            h.add_symbol(sym);
        }
        h
    }

    // ------------------------------------------------------------------
    // Symbol registration
    // ------------------------------------------------------------------

    /// Register a single symbol.  No-op if already registered.
    pub fn add_symbol(&mut self, symbol: impl Into<String>) -> &mut Self {
        let s = symbol.into();
        self.books
            .entry(s.clone())
            .or_insert_with(|| OrderBook::new(s));
        self
    }

    /// Register `n` symbols named `"SYMnnnnnn"`.
    pub fn add_symbol_count(&mut self, n: usize) -> &mut Self {
        for i in 1..=n {
            self.add_symbol(format!("SYM{i:06}"));
        }
        self
    }

    // ------------------------------------------------------------------
    // Per-symbol event application
    // ------------------------------------------------------------------

    /// Apply a [`BookSnapshot`] with `bid_levels` bid levels and `ask_levels`
    /// ask levels.  Clears the existing book state.
    pub fn apply_snapshot_for(&mut self, symbol: &str, bid_levels: usize, ask_levels: usize) {
        let uid = self.next_seq();
        let bids = make_bid_levels(&mut self.prng, bid_levels);
        let asks = make_ask_levels(&mut self.prng, ask_levels);
        let snap = make_snapshot(symbol, uid, bids, asks);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    /// Apply a [`BookDelta`] with `bid_levels` bid updates and `ask_levels` ask
    /// updates.  Prices are distinct from the current book so they always add
    /// new levels (not updates or removals).
    pub fn apply_delta_for(&mut self, symbol: &str, bid_levels: usize, ask_levels: usize) {
        let uid = self.next_seq();
        let bids = make_bid_levels(&mut self.prng, bid_levels);
        let asks = make_ask_levels(&mut self.prng, ask_levels);
        let d = make_delta(symbol, uid, bids, asks);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_delta(&d);
            self.stats.deltas_applied += 1;
        }
    }

    /// Apply a delta with **zero** bid and ask changes.
    ///
    /// `last_update_id` advances but no levels are added, modified, or removed.
    pub fn apply_zero_update(&mut self, symbol: &str) -> u64 {
        let uid = self.next_seq();
        let d = make_delta(symbol, uid, vec![], vec![]);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_delta(&d);
            self.stats.deltas_applied += 1;
        }
        uid
    }

    /// Replace the book with a snapshot where the bid is [`WIDE_BID_PRICE`]
    /// and the ask is [`WIDE_ASK_PRICE`] — the widest representable spread.
    pub fn apply_wide_spread(&mut self, symbol: &str) {
        let uid = self.next_seq();
        let bids = vec![PriceLevel {
            price: WIDE_BID_PRICE,
            qty: self.prng.qty(),
        }];
        let asks = vec![PriceLevel {
            price: WIDE_ASK_PRICE,
            qty: self.prng.qty(),
        }];
        let snap = make_snapshot(symbol, uid, bids, asks);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    /// Replace the book with a snapshot that has **no bids** (asks only).
    pub fn apply_empty_bid_side(&mut self, symbol: &str) {
        let uid = self.next_seq();
        let asks = make_ask_levels(&mut self.prng, 5);
        let snap = make_snapshot(symbol, uid, vec![], asks);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    /// Replace the book with a snapshot that has **no asks** (bids only).
    pub fn apply_empty_ask_side(&mut self, symbol: &str) {
        let uid = self.next_seq();
        let bids = make_bid_levels(&mut self.prng, 5);
        let snap = make_snapshot(symbol, uid, bids, vec![]);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    /// Clear all levels by applying an empty snapshot (simulates delisting).
    ///
    /// After this call `book.is_empty()` is `true`.
    pub fn apply_delisting(&mut self, symbol: &str) {
        let uid = self.next_seq();
        let snap = make_snapshot(symbol, uid, vec![], vec![]);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    /// Apply a single delta with `level_count` bid levels and `level_count` ask
    /// levels — exercises bulk insertion performance.
    pub fn apply_large_batch(&mut self, symbol: &str, level_count: usize) {
        let uid = self.next_seq();
        let bids = (0..level_count)
            .map(|i| PriceLevel {
                price: MID_PRICE - TICK_SIZE * (i as i64 + 1),
                qty: self.prng.qty(),
            })
            .collect();
        let asks = (0..level_count)
            .map(|i| PriceLevel {
                price: MID_PRICE + TICK_SIZE * (i as i64 + 1),
                qty: self.prng.qty(),
            })
            .collect();
        let snap = make_snapshot(symbol, uid, bids, asks);
        if let Some(book) = self.books.get_mut(symbol) {
            book.apply_snapshot(&snap);
            self.stats.snapshots_applied += 1;
        }
    }

    // ------------------------------------------------------------------
    // Broadcast — apply the same event to ALL symbols
    // ------------------------------------------------------------------

    /// Apply `apply_snapshot_for` to every registered symbol.
    ///
    /// Symbols are processed in sorted order to guarantee a deterministic
    /// PRNG sequence for a given seed.
    pub fn broadcast_snapshot(&mut self, bid_levels: usize, ask_levels: usize) {
        let mut symbols: Vec<String> = self.books.keys().cloned().collect();
        symbols.sort_unstable();
        for sym in symbols {
            self.apply_snapshot_for(&sym, bid_levels, ask_levels);
        }
    }

    /// Apply `apply_delta_for` to every registered symbol.
    ///
    /// Symbols are processed in sorted order to guarantee determinism.
    pub fn broadcast_delta(&mut self, bid_levels: usize, ask_levels: usize) {
        let mut symbols: Vec<String> = self.books.keys().cloned().collect();
        symbols.sort_unstable();
        for sym in symbols {
            self.apply_delta_for(&sym, bid_levels, ask_levels);
        }
    }

    /// Apply `apply_zero_update` to every registered symbol.
    ///
    /// Symbols are processed in sorted order to guarantee determinism.
    pub fn broadcast_zero_update(&mut self) {
        let mut symbols: Vec<String> = self.books.keys().cloned().collect();
        symbols.sort_unstable();
        for sym in symbols {
            self.apply_zero_update(&sym);
        }
    }

    // ------------------------------------------------------------------
    // Accessors
    // ------------------------------------------------------------------

    pub fn book(&self, symbol: &str) -> Option<&OrderBook> {
        self.books.get(symbol)
    }

    pub fn symbol_count(&self) -> usize {
        self.books.len()
    }

    pub fn current_seq(&self) -> u64 {
        self.seq
    }

    // ------------------------------------------------------------------
    // Invariant checking
    // ------------------------------------------------------------------

    /// Scan all books for invariant violations.
    ///
    /// Checks:
    /// 1. `best_bid.price < best_ask.price` when both sides are non-empty.
    /// 2. Every retained level has `qty > 0`.
    pub fn check_invariants(&self) -> InvariantViolations {
        let mut out = InvariantViolations::default();
        for (sym, book) in &self.books {
            // 1. Crossed book
            if let (Some(bb), Some(ba)) = (book.best_bid(), book.best_ask()) {
                if bb.price >= ba.price {
                    out.violations.push(InvariantViolation::BidAskCrossed {
                        symbol: sym.clone(),
                        bid_price: bb.price,
                        ask_price: ba.price,
                    });
                }
            }
            // 2. Non-positive quantities
            for lvl in book.bids() {
                if lvl.qty <= 0 {
                    out.violations.push(InvariantViolation::NonPositiveQty {
                        symbol: sym.clone(),
                        side: "bid",
                        price: lvl.price,
                        qty: lvl.qty,
                    });
                }
            }
            for lvl in book.asks() {
                if lvl.qty <= 0 {
                    out.violations.push(InvariantViolation::NonPositiveQty {
                        symbol: sym.clone(),
                        side: "ask",
                        price: lvl.price,
                        qty: lvl.qty,
                    });
                }
            }
        }
        out
    }

    // ------------------------------------------------------------------
    // Private helpers
    // ------------------------------------------------------------------

    fn next_seq(&mut self) -> u64 {
        self.seq += 1;
        self.seq
    }
}

// ---------------------------------------------------------------------------
// Free-function builders
// ---------------------------------------------------------------------------

fn make_header(msg_type: MessageType) -> MessageHeader {
    MessageHeader {
        schema_version: SCHEMA_VERSION,
        message_type: msg_type,
        venue_id: VenueId::BinanceSpot,
        market_type: MarketType::Spot,
        instrument_id: 0,
        connection_id: 0,
        instance_id: 0,
        sequence_number: 0,
        exchange_event_ts: TS_NONE,
        exchange_tx_ts: TS_NONE,
        local_recv_ts: TS_NONE,
        local_publish_ts: TS_NONE,
    }
}

fn make_bid_levels(prng: &mut Prng, count: usize) -> Vec<PriceLevel> {
    (0..count)
        .map(|i| PriceLevel {
            price: MID_PRICE - TICK_SIZE * (i as i64 + 1),
            qty: prng.qty(),
        })
        .collect()
}

fn make_ask_levels(prng: &mut Prng, count: usize) -> Vec<PriceLevel> {
    (0..count)
        .map(|i| PriceLevel {
            price: MID_PRICE + TICK_SIZE * (i as i64 + 1),
            qty: prng.qty(),
        })
        .collect()
}

fn make_snapshot(
    symbol: &str,
    uid: u64,
    bids: Vec<PriceLevel>,
    asks: Vec<PriceLevel>,
) -> BookSnapshot {
    BookSnapshot {
        header: make_header(MessageType::BookSnapshot),
        symbol: symbol.into(),
        price_scale: 2,
        qty_scale: 3,
        update_id: uid,
        bids,
        asks,
    }
}

fn make_delta(symbol: &str, uid: u64, bids: Vec<PriceLevel>, asks: Vec<PriceLevel>) -> BookDelta {
    BookDelta {
        header: make_header(MessageType::BookDelta),
        symbol: symbol.into(),
        price_scale: 2,
        qty_scale: 3,
        first_update_id: uid,
        final_update_id: uid,
        prev_update_id: UPDATE_ID_NONE,
        bids,
        asks,
    }
}

