    #[test]
    fn invariant_check_detects_crossed_book_from_direct_snapshot() {
        // Construct a crossed book by providing a snapshot where bid > ask.
        // The OrderBook stores what it's told; the invariant checker catches it.
        let mut h = SyntheticHarness::with_symbols(["BTCUSDT"], 77);

        // Feed a crossed snapshot directly (bid price > ask price).
        let uid = h.next_seq();
        let snap = make_snapshot(
            "BTCUSDT",
            uid,
            vec![PriceLevel { price: 200, qty: 1 }], // bid = 200
            vec![PriceLevel { price: 100, qty: 1 }], // ask = 100  ← crossed
        );
        h.books.get_mut("BTCUSDT").unwrap().apply_snapshot(&snap);
        h.stats.snapshots_applied += 1;

        let v = h.check_invariants();
        assert!(!v.is_clean(), "crossed book must be detected");
        assert_eq!(v.count(), 1);
        assert!(matches!(
            &v.violations[0],
            InvariantViolation::BidAskCrossed {
                bid_price: 200,
                ask_price: 100,
                ..
            }
        ));
    }

    #[test]
    fn invariant_check_finds_multiple_violations_across_symbols() {
        let mut h = SyntheticHarness::with_symbol_count(3, 88);

        // Set up two crossed books.
        for sym in ["SYM000001", "SYM000002"] {
            let uid = h.next_seq();
            let snap = make_snapshot(
                sym,
                uid,
                vec![PriceLevel { price: 500, qty: 1 }],
                vec![PriceLevel { price: 100, qty: 1 }],
            );
            h.books.get_mut(sym).unwrap().apply_snapshot(&snap);
            h.stats.snapshots_applied += 1;
        }

        // SYM000003 is fine.
        h.apply_snapshot_for("SYM000003", 3, 3);

        let v = h.check_invariants();
        assert_eq!(v.count(), 2, "expected exactly 2 crossed-book violations");
    }

